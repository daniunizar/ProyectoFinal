    public static void extraerDatosDeBase() {
        miLista = new ArrayList<>();
        ConexionBaseDatos.ConexionBaseDatos();
        Connection conexion = ConexionBaseDatos.getConnection();
        Statement sentencia;
        try {
            sentencia = conexion.createStatement();
            String sql;
            sql = "SELECT * FROM REGISTRO_ENTRADAS ORDER BY NUM_REF DESC";
            ResultSet resul = sentencia.executeQuery(sql);
            while (resul.next()) {
                //TIPO ENTRADA, CANTIDAD, EXENCIONES, PRECIO TOTAL
                Registro_Entradas registro = new Registro_Entradas(resul.getInt(1), Fecha.formateaFecha(resul.getDate(2)), resul.getString(3), resul.getInt(4), resul.getInt(5), resul.getFloat(6));
                miLista.add(registro);
            }
        } catch (SQLException ex) {
            Logger.getLogger(F_Historico_Visitantes.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("Error al tratar de conectar con la base de datos desde el Histórico de Venta de Entradas");
        }

        ConexionBaseDatos.desconexionBaseDatos();
    }

    public static void rellenarTabla() {
        extraerDatosDeBase();
        String matrizDatos[][] = new String[miLista.size()][6];//el segundo número es el número de campos
        for (int i = 0; i < miLista.size(); i++) {
            matrizDatos[i][0] = Integer.toString(miLista.get(i).getNum_ref());
            matrizDatos[i][1] = miLista.get(i).getFecha();
            matrizDatos[i][2] = miLista.get(i).getTipoEntrada();
            matrizDatos[i][3] = Integer.toString(miLista.get(i).getCantidad());
            matrizDatos[i][4] = Integer.toString(miLista.get(i).getExcenciones());
            matrizDatos[i][5] = Float.toString(miLista.get(i).getPrecioTotal());
        }

        tabla_Historico_Entradas.setModel(new javax.swing.table.DefaultTableModel(
                matrizDatos,
                new String[]{
                    "NUM_REG", "FECHA", "TIPO ENTRADA", "CANTIDAD", "EXENCIONES", "PRECIO TOTAL"
                }
        ));
    }

    public void eliminarRegistros(int indice) {
        ConexionBaseDatos.ConexionBaseDatos();
        Connection conexion = ConexionBaseDatos.getConnection();
        Statement sentencia;
        try {
            sentencia = conexion.createStatement();
            String sql;
            sql = "DELETE FROM REGISTRO_ENTRADAS WHERE NUM_REF =" + indice;
            ResultSet resul = sentencia.executeQuery(sql);
            System.out.println("Se ha eliminado el registro " + indice);
        } catch (SQLException ex) {
            Logger.getLogger(F_Historico_Entradas.class.getName()).log(Level.SEVERE, null, ex);
        }

        rellenarTabla();
    }

    public static void establecerColor() {
        ColorInterfaz.dimeColores();
        Color colorFondo = ColorInterfaz.dimeColores()[0];
        Color colorBotones = ColorInterfaz.dimeColores()[1];
        Color colorTexto = ColorInterfaz.dimeColores()[2];

        //Paneles de fondo
        panelFondo.setBackground(colorFondo);

        //Botones
        btnModificar.setBackground(colorBotones);
        btnEliminar.setBackground(colorBotones);

        //Textos
        labelTitulo.setForeground(colorTexto);
        btnModificar.setForeground(colorTexto);
        btnEliminar.setForeground(colorTexto);
        if (modificarEntradas != null) modificarEntradas.establecerColor();
    }
