    /**
     * Este método realiza una consulta a la Base de Datos para obtener de ella los diferentes productos promocionales existentes.
     */
    public static void extraerDatosDeBase() {
        miLista = new ArrayList<>();
        ConexionBaseDatos.ConexionBaseDatos();
        Connection conexion = ConexionBaseDatos.getConnection();
        Statement sentencia;
        try {
            sentencia = conexion.createStatement();
            String sql;
            sql = "SELECT * FROM MERCHANDISING ORDER BY CONCEPTO ASC";
            ResultSet resul = sentencia.executeQuery(sql);
            while (resul.next()) {
                TipoMerchandising entrada = new TipoMerchandising(resul.getString(1), resul.getFloat(2));
                miLista.add(entrada);
            }
        } catch (SQLException ex) {
            System.out.println("Error al tratar de conectar con la base de datos desde Configuración de merchandising");
        }

        ConexionBaseDatos.desconexionBaseDatos();
    }

    /**
     * Este método rellena el jTable con los productos promocionales existentes, extraidos de la Base de Datos mediante el método extraerDatosDeBase().
     */
    public static void rellenarTabla() {
        extraerDatosDeBase();
        String matrizDatos[][] = new String[miLista.size()][2];//el segundo número es el número de campos
        for (int i = 0; i < miLista.size(); i++) {
            matrizDatos[i][0] = miLista.get(i).getConcepto();
            matrizDatos[i][1] = Float.toString(miLista.get(i).getPrecio());
        }

        tablaMerchandising.setModel(new javax.swing.table.DefaultTableModel(
                matrizDatos,
                new String[]{
                    "CONCEPTO", "PRECIO"
                }
        ));
    }

    /**
     * Este método procede, previa confirmación por parte del usuario, a la eliminación de los productos promocionales seleccionados.
     * @param indice Es la clave prmaria del tipo de producto promocional que se pretende eliminar.
     */
    public void eliminarRegistros(String indice) {
        ConexionBaseDatos.ConexionBaseDatos();
        Connection conexion = ConexionBaseDatos.getConnection();
        Statement sentencia;
        try {
            sentencia = conexion.createStatement();
            String sql;
            sql = "DELETE FROM MERCHANDISING WHERE CONCEPTO = '" + indice + "'";
            ResultSet resul = sentencia.executeQuery(sql);
            System.out.println("Se ha eliminado el registro " + indice);
        } catch (SQLException ex) {
            System.out.println("error al tratar de borrar un tipo de objeto promocional");
        }

        rellenarTabla();
    }

    /**
     * Este método gestiona el color de los componentes de la interfaz gráfica que muestra los tipos de productos promocionales existentes.
     */
    public static void establecerColor() {
        ColorInterfaz.dimeColores();
        Color colorFondo = ColorInterfaz.dimeColores()[0];
        Color colorBotones = ColorInterfaz.dimeColores()[1];
        Color colorTexto = ColorInterfaz.dimeColores()[2];

        //Paneles de fondo
        panelFondo.setBackground(colorFondo);

        //Botones
        btnNuevo.setBackground(colorBotones);
        btnModificar.setBackground(colorBotones);
        btnEliminar.setBackground(colorBotones);

        //Textos
        labelTitulo.setForeground(colorTexto);
        btnNuevo.setForeground(colorTexto);
        btnModificar.setForeground(colorTexto);
        btnEliminar.setForeground(colorTexto);
        if (nuevoMerchandising != null) {
            nuevoMerchandising.establecerColor();
        }
        if (modificarMerchandising != null) {
            modificarMerchandising.establecerColor();
        }
    }
